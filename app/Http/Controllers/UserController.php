    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $input = $request->all();

        $this->validate($request, [
            'name' => 'required',
            'email' => 'required',
            'password' => 'required',
            'role' => 'required',
        ]);

        if ($input['password'] == $input['password2']) {
            unset($input['password2']);
            User::create([
                'name' => $input['name'],
                'email' => $input['email'],
                'role' => $input['role'],
                'password' => Hash::make($input['password']),
            ]);
            return redirect()->route('dataUser.index')->with('success', 'User created succesfully');
        }else{
            return redirect()->route('dataUser.index')->with('error', 'passwords do not match');
        }
    }

